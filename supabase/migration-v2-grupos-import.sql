-- 5. Inserir pessoas (unique por nome+grupo)
INSERT INTO pessoas (nome, grupo)
SELECT DISTINCT nome, grupo FROM csv_data
ON CONFLICT DO NOTHING;

-- 6. Inserir chamadas
INSERT INTO chamadas (data)
SELECT DISTINCT data FROM csv_data
ON CONFLICT (data) DO NOTHING;

-- 7. Inserir presencas
INSERT INTO presencas (chamada_id, pessoa_id, presente)
SELECT c.id, p.id, (csv.presenca = 1)
FROM csv_data csv
JOIN pessoas p ON p.nome = csv.nome AND p.grupo = csv.grupo
JOIN chamadas c ON c.data = csv.data;

-- 8. Inserir cestas (onde cesta = 1)
INSERT INTO cestas (pessoa_id, data)
SELECT p.id, csv.data
FROM csv_data csv
JOIN pessoas p ON p.nome = csv.nome AND p.grupo = csv.grupo
WHERE csv.cesta = 1;

